public class HomeWork06 {
    public static void main(String[] args) {

        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + "\t");
        }
        System.out.println("\n" + "----------------------------");
        for (char j = 'Z'; j >= 'A'; j--) {
            System.out.print(j + "\t");
        }


    }
}
